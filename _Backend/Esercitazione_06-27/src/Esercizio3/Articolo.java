package Esercizio3;

public class Articolo {
	String codex;
	String description;
	double price;
	int availableProds;
	
	public Articolo(String _codex, String _description, double _price, int _availableProds) {
		this.codex = _codex;
		this.description = _description;
		this.price = _price;
		this.availableProds = _availableProds;
	}
}
